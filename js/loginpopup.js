

function logintoggle(){
	var registerLogin = document.getElementById('loginpopup');

	if( registerLogin.style.display = "none"){
		registerLogin.style.display = "inline";
	}
}

function closelogin(){
	var registerLogin = document.getElementById('loginpopup');

	if( registerLogin.style.display = "inline"){
		registerLogin.style.display = "none";
	}
}